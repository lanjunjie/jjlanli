package com.myfp.myfund.api.beans;


public class GroupChar {
	private double CombContrast;
	private double CombIndex;
	private String CombName;
	private String ContrastName;
	private String Date;
	public double getCombContrast() {
		return CombContrast;
	}
	public void setCombContrast(double combContrast) {
		CombContrast = combContrast;
	}
	public double getCombIndex() {
		return CombIndex;
	}
	public void setCombIndex(double combIndex) {
		CombIndex = combIndex;
	}
	public String getCombName() {
		return CombName;
	}
	public void setCombName(String combName) {
		CombName = combName;
	}
	public String getContrastName() {
		return ContrastName;
	}
	public void setContrastName(String contrastName) {
		ContrastName = contrastName;
	}
	public String getDate() {
		return Date;
	}
	public void setDate(String date) {
		Date = date;
	}
}
