package com.myfp.myfund.api.beans;

public class CurrentProductResult {
	private String Fundcode;
	private String Manager;
	private String FundName;
	private String ThisYearYield;
	private String OneYearYield;
	private String Est_date;
	private String DealDate;
	private String UnitEquity;
	public String getFundcode() {
		return Fundcode;
	}
	public void setFundcode(String fundcode) {
		Fundcode = fundcode;
	}
	public String getManager() {
		return Manager;
	}
	public void setManager(String manager) {
		Manager = manager;
	}
	public String getFundName() {
		return FundName;
	}
	public void setFundName(String fundName) {
		FundName = fundName;
	}
	public String getThisYearYield() {
		return ThisYearYield;
	}
	public void setThisYearYield(String thisYearYield) {
		ThisYearYield = thisYearYield;
	}
	public String getOneYearYield() {
		return OneYearYield;
	}
	public void setOneYearYield(String oneYearYield) {
		OneYearYield = oneYearYield;
	}
	public String getEst_date() {
		return Est_date;
	}
	public void setEst_date(String est_date) {
		Est_date = est_date;
	}
	public String getDealDate() {
		return DealDate;
	}
	public void setDealDate(String dealDate) {
		DealDate = dealDate;
	}
	public String getUnitEquity() {
		return UnitEquity;
	}
	public void setUnitEquity(String unitEquity) {
		UnitEquity = unitEquity;
	}
	
	

}
